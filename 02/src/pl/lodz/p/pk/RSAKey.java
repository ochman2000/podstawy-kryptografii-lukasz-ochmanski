package pl.lodz.p.pk;

public class RSAKey {
	private int publiczny;
	private int prywatny;
	
	public RSAKey() {
		
	}
	
	public int getPubliczny() {
		return publiczny;
	}
	public void setPubliczny(int publiczny) {
		this.publiczny = publiczny;
	}

	public int getPrywatny() {
		return prywatny;
	}

	public void setPrywatny(int prywatny) {
		this.prywatny = prywatny;
	}

}
